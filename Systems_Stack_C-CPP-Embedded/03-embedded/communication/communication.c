// ============================================================
// UART SIMULATION
// ============================================================

typedef struct {
    volatile uint32_t SR;   // Status register
    volatile uint32_t DR;   // Data register
    volatile uint32_t BRR;  // Baud rate register
    volatile uint32_t CR1;  // Control register 1
    volatile uint32_t CR2;  // Control register 2
    volatile uint32_t CR3;  // Control register 3
} USART_TypeDef;

USART_TypeDef* USART2 = (USART_TypeDef*)0x40004400;

// Simulated UART transmit
void uart_send_char(char c) {
    printf("%c", c);
    USART2->DR = c;
    USART2->SR |= 0x0080;  // TXE flag
}

void uart_send_string(const char* str) {
    while(*str) {
        uart_send_char(*str);
        str++;
    }
}

void uart_send_hex(uint32_t value) {
    char buffer[16];
    sprintf(buffer, "0x%08X", value);
    uart_send_string(buffer);
}

// ============================================================
// SPI SIMULATION
// ============================================================

typedef struct {
    volatile uint32_t CR1;   // Control register 1
    volatile uint32_t CR2;   // Control register 2
    volatile uint32_t SR;    // Status register
    volatile uint32_t DR;    // Data register
} SPI_TypeDef;

SPI_TypeDef* SPI1 = (SPI_TypeDef*)0x40013000;

uint8_t spi_transfer(uint8_t data_out) {
    SPI1->DR = data_out;
    // Wait for transfer complete (simulated)
    usleep(100);
    return SPI1->DR;
}

